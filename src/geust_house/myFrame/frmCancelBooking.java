    private void DateToPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_DateToPropertyChange
       if("date".equals(evt.getPropertyName())){
           try{
               JDateChooser dateTo=(JDateChooser) evt.getSource();
              String dateToFormat=new SimpleDateFormat("YYYY-MM-DD").format(dateTo.getDate());
              if(dateToFormat.compareTo(dateOut.toString())>0||dateToFormat.compareTo(dateIn.toString())<=0){
                  JOptionPane.showMessageDialog(this,"Out of book date : "+dateIn+" - "+dateOut);
                        dateTo.setDate(null);
              }

           }catch(Exception ex){}
       }
    }//GEN-LAST:event_DateToPropertyChange

    private void DateFromPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_DateFromPropertyChange
        if("date".equals(evt.getPropertyName())){ 
       
                try{
                    JDateChooser dateFrom= (JDateChooser) evt.getSource();
                    if(dateFrom.getDate().compareTo(dateIn)<0 ||dateFrom.getDate().compareTo(dateOut)>=0){
                       JOptionPane.showMessageDialog(this,"Out of book date : "+dateIn+" - "+dateOut);
                        dateFrom.setDate(null);
                    }
                    
                }catch(Exception ex){}
                

           
           
           
       }
    }//GEN-LAST:event_DateFromPropertyChange

