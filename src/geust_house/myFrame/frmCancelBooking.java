    private void ChangeRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangeRoomActionPerformed
        try {
            //duration of cancel book
            int diff=(int) ((DateTo.getDate().getTime()-DateFrom.getDate().getTime())/(1000*3600*24));
            JOptionPane.showMessageDialog(this,DateTo.getDate().getTime());
            JOptionPane.showMessageDialog(this,DateFrom.getDate().getTime());
            JOptionPane.showMessageDialog(this,diff);
            //insert into cancel booking
            pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_INTO_CANCELBOOK);
            pst.setString(1,df.format(cancelDate));
            pst.setInt(2, 1);
            pst.setInt(3, book_id);
            pst.setInt(4,cus_id);
             JOptionPane.showMessageDialog(this,df.format(cancelDate)+ book_id+cus_id);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(this,"insert into cancel book success");
            //get cancel book id
            int cancel_book_id=0;
            pst=data.getConnectio().prepareStatement(SqlConfigString.MAX_ID_CANCELBOOK);
            res=pst.executeQuery();
            while(res.next()){
                cancel_book_id=res.getInt(1);
            }
            res.close();
            //if the customer call to cancel b4 book check in date the refund will give 
            if(cancelDate.compareTo(dateIn)>=0){
                price=0;
            }
            //insert into cancel book detail
            pst=data.getConnectio().prepareStatement(SqlConfigString.INSERT_INTO_CANCELBOOK_DETAIL);
            pst.setInt(1, cancel_book_id);
            pst.setInt(2, room_id);
            pst.setInt(3, diff);
            pst.setFloat(4,price);
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(this,"insert into cancel book detail success");
            //Update status in Booking
            SimpleDateFormat dc=new SimpleDateFormat("YYYY-MM-DD");
            if(dc.format(DateFrom.getDate()).compareTo(dateIn.toString())==0 && dc.format(DateTo.getDate()).compareTo(dateOut.toString())==0){
                pst=data.getConnectio().prepareStatement(SqlConfigString.UPDATE_STATUS_BOOK_DETAIL);
                pst.setString(1,"Cancel Booking");
                pst.setInt(2, book_id);
                pst.execute();
                pst.close();
                JOptionPane.showMessageDialog(this,"Update Sucessfully");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmCancelBooking.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmCancelBooking.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(frmCancelBooking.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(frmCancelBooking.class.getName()).log(Level.SEVERE, null, ex);
        }
        
            
    }//GEN-LAST:event_ChangeRoomActionPerformed

